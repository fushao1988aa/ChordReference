//==================================================================================================
//  TSDBuilder.h
//  项目：ChordReference (和弦调性速查器)
//  功能：构建 T（主和弦）、S（下属和弦）、D（属和弦）家族
//        - 大调：I, IV, V
//        - 小调：i, iv, V（和声小调属和弦升高七级）
//        - 支持全部七种调式
//  注意：需保存为 UTF-8 with BOM
//==================================================================================================

#pragma once

#include "MusicTypes.h"
#include "ScaleEngine.h"

//==============================================================================
// T/S/D 三和弦家族数据结构
//==============================================================================
struct TSDFamily
{
    Chord tonic;
    Chord subdominant;
    Chord dominant;
};

class TSDBuilder
{
public:
    //==============================================================================
    // 大调 T/S/D（自然大调 I, IV, V）
    //==============================================================================
    static TSDFamily getMajor(PitchClass tonic);

    //==============================================================================
    // 小调 T/S/D（自然小调 i, iv, 和声小调 V）
    //==============================================================================
    static TSDFamily getMinor(PitchClass tonic);

    //==============================================================================
    // 通用调式 T/S/D（支持全部 ScaleType）
    //==============================================================================
    static TSDFamily getFamily(PitchClass tonic, ScaleType type);
};