//==================================================================================================
//  PitchNameHelper.h
//  项目：ChordReference (和弦调性速查器)
//  功能：音名转换与中文字体加载辅助类
//        - 根据音高类返回升号/降号记谱名
//        - 判断主音属于升号调还是降号调
//        - 全局加载鸿蒙中文字体（Typeface），所有 UI 共享
//  注意：需保存为 UTF-8 with BOM
//==================================================================================================

#pragma once

#include "MusicTypes.h"
#include <string>
#include <juce_graphics/juce_graphics.h>

class PitchNameHelper
{
public:
    //==============================================================================
    // 音名获取
    //   pitch     : 音高类 (0~11)
    //   isSharpKey: true → 升号名 (C, C#, D, D# ...)
    //                false → 降号名 (C, Db, D, Eb ...)
    //==============================================================================
    static std::string getNameForPitch(PitchClass pitch, bool isSharpKey);

    //==============================================================================
    // 判断主音更倾向升号调还是降号调
    //   true  → 升号调（C, G, D, A, E, B, F#, C#）
    //   false → 降号调（F, Bb, Eb, Ab, Db, Gb, Cb）
    //==============================================================================
    static bool isSharpKeyForTonic(PitchClass tonic);

    //==============================================================================
    // 获取全局共享的鸿蒙中文字体
    //   从 BinaryData 加载 HarmonyOS_Sans_SC_Regular.ttf
    //   失败则降级为系统默认无衬线字体（不崩溃）
    //   首次调用后缓存，后续直接返回
    //==============================================================================
    static juce::Typeface::Ptr getHarmonyTypeface();
};